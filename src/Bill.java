    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
          
        String saleId = jTextField1.getText(); 
        try {
            if (con == null) {
                con = connect();
            }
            String query = "SELECT s.sale_id, s.sale_price, s.quantity, c.name, c.address, "
                    + "l.brand, l.model "
                    + "FROM sales s "
                    + "JOIN customers c ON s.customer_id = c.customer_id "
                    + "JOIN laptops l ON s.laptop_id = l.laptop_id "
                    + "WHERE s.sale_id = ?";
            pst = con.prepareStatement(query);
            pst.setString(1, saleId);
            rs = pst.executeQuery();
            if (rs.next()) {
                String customerName = rs.getString("name");
                String customerAddress = rs.getString("address");
                String laptopModel = rs.getString("model");
                String laptopBrand = rs.getString("brand");
                String salePrice = rs.getString("sale_price");
                String quantityInput = JOptionPane.showInputDialog(null, "Enter the number of items:");
                int quantity = Integer.parseInt(quantityInput);
                double totalAmount = Double.parseDouble(salePrice) * quantity;
                Document document = new Document();
                PdfWriter.getInstance(document, new FileOutputStream("Invoice_" + saleId + ".pdf"));
                document.open();
                Paragraph title = new Paragraph("Ripples Solutions", FontFactory.getFont(FontFactory.HELVETICA_BOLD, 18));
                title.setAlignment(Element.ALIGN_CENTER);
                document.add(title);
                Paragraph contact = new Paragraph("Contact: +91-9876543210 | Email: info@ripplessolutions.com\n\n", FontFactory.getFont(FontFactory.HELVETICA, 12));
                contact.setAlignment(Element.ALIGN_CENTER);
                document.add(contact);
                document.add(new Paragraph("Date: " + new SimpleDateFormat("dd/MM/yyyy").format(new Date()) + "\n\n"));
                Paragraph invoiceTitle = new Paragraph("INVOICE", FontFactory.getFont(FontFactory.HELVETICA_BOLD, 16));
                invoiceTitle.setAlignment(Element.ALIGN_CENTER);
                document.add(invoiceTitle);
                document.add(new Paragraph("\n"));
                document.add(new Paragraph("Customer Name: " + customerName));
                document.add(new Paragraph("Customer Address: " + customerAddress + "\n\n"));
                PdfPTable table = new PdfPTable(4);
                table.setWidthPercentage(100);
                table.setSpacingBefore(10f);
                table.setSpacingAfter(10f);
                table.addCell(new PdfPCell(new Phrase("Brand", FontFactory.getFont(FontFactory.HELVETICA_BOLD))));
                table.addCell(new PdfPCell(new Phrase("Model", FontFactory.getFont(FontFactory.HELVETICA_BOLD))));
                table.addCell(new PdfPCell(new Phrase("Unit Price", FontFactory.getFont(FontFactory.HELVETICA_BOLD))));
                table.addCell(new PdfPCell(new Phrase("Quantity", FontFactory.getFont(FontFactory.HELVETICA_BOLD))));
                table.addCell(laptopBrand);
                table.addCell(laptopModel);
                table.addCell("₹" + salePrice);
                table.addCell(String.valueOf(quantity));
                document.add(table);
                Paragraph total = new Paragraph("Total Amount: ₹" + totalAmount, FontFactory.getFont(FontFactory.HELVETICA_BOLD, 14));
                total.setAlignment(Element.ALIGN_RIGHT);
                document.add(total);
                Paragraph thanks = new Paragraph("\nThank you for your purchase!", FontFactory.getFont(FontFactory.HELVETICA_OBLIQUE, 12));
                thanks.setAlignment(Element.ALIGN_CENTER);
                document.add(thanks);
                document.close();
                JOptionPane.showMessageDialog(null, "Invoice generated successfully!");
            } else {
                JOptionPane.showMessageDialog(null, "Sale ID not found.");
            }
            rs.close();
            pst.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error generating invoice: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

