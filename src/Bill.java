    @SuppressWarnings("FinallyDiscardsException")
    public final void fetchDetailsFromSaleId(int saleId) throws SQLException {
        if (con != null) {
            try {
                String query = "SELECT c.customer_id, l.laptop_id, l.brand, s.sale_price, s.quantity "
                + "FROM customers c "
                + "JOIN sales s ON c.customer_id = s.customer_id "
                + "JOIN laptops l ON s.laptop_id = l.laptop_id "
                + "WHERE s.sale_id = ?";
                PreparedStatement ps = con.prepareStatement(query);
                ps.setInt(1, saleId); 
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    jTextField2.setText(rs.getString("customer_id"));  
                    jTextField3.setText(rs.getString("laptop_id"));    
                    jTextField4.setText(rs.getString("brand"));        
                    jTextField5.setText(rs.getString("sale_price"));        
                    jTextField6.setText(rs.getString("quantity"));     
                } else {
                    JOptionPane.showMessageDialog(this, "Sale ID not found!", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            } finally {
                return;
            }
        }
    }
