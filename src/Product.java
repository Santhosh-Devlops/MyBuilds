    private void updateStockInDatabase(String laptopID, String laptopName, int model, double price, int stockQuantity) {
        try {
            pst = con.prepareStatement("SELECT stock_quantity FROM laptops WHERE brand = ?");
            pst.setString(1, laptopName);
            rs = pst.executeQuery();
            if (rs.next()) {
                int existingStock = rs.getInt("stock_quantity");
                int newStock = existingStock + stockQuantity;
                pst = con.prepareStatement("UPDATE laptops SET stock_quantity = ? WHERE brand = ?");
                pst.setInt(1, newStock);
                pst.setString(2, laptopName);
                pst.executeUpdate();
            } else {
                pst = con.prepareStatement("INSERT INTO laptops (laptop_id, brand, model, price, stock_quantity) VALUES (?, ?, ?, ?, ?)");
                pst.setString(1, laptopID);
                pst.setString(2, laptopName);
                pst.setInt(3, model);
                pst.setDouble(4, price);
                pst.setInt(5, stockQuantity);
                pst.executeUpdate();
            }
            JOptionPane.showMessageDialog(this, "Stock updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }   
    private void deleteLaptopRecord() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String brand = jTable1.getValueAt(selectedRow, 2).toString();
            int stockToRemove = Integer.parseInt(jTable1.getValueAt(selectedRow, 4).toString());

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.removeRow(selectedRow);
            try {
                pst = con.prepareStatement("UPDATE laptops SET stock_quantity = stock_quantity - ? WHERE brand = ?");
                pst.setInt(1, stockToRemove);
                pst.setString(2, brand);
                pst.executeUpdate();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error updating stock: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
