    private void jTextField5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField5ActionPerformed
        // TODO add your handling code here:
        String stockText=jTextField5.getText();
        if(stockText.isEmpty()){
            JOptionPane.showMessageDialog(this,"Stock quantity should be entered !","Error",JOptionPane.ERROR_MESSAGE);
        }
        try {
            int stockQuantity = Integer.parseInt(stockText);
            String laptopID = jTextField1.getText();
            String laptopName = jTextField2.getText();
            String model = jTextField3.getText();
            double price = Double.parseDouble(jTextField4.getText());

            DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();
            boolean brandExists = false;
            for (int i = 0; i < model1.getRowCount(); i++) {
                if (!model1.getValueAt(i, 2).equals(laptopName)) {
                } else {
                    int currentStock = (int) model1.getValueAt(i, 4);
                    model1.setValueAt(currentStock + stockQuantity, i, 4);
                    brandExists = true;
                    break;
                }
            }
            if (!brandExists) {
                model1.addRow(new Object[]{laptopID, laptopName, model, price, stockQuantity});
            }
            updateStockInDatabase(laptopID, laptopName, model, price, stockQuantity);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid stock quantity! Enter a number.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jTextField5ActionPerformed

