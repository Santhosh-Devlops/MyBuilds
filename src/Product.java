    private void loadlaptops() {
        try {
            pst = con.prepareStatement("SELECT * FROM laptops");
            rs = pst.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            while (rs.next()) {
                Object[] row = {
                    rs.getInt("laptop_id"),
                    rs.getString("brand"),
                    rs.getString("model"),
                    rs.getString("price"),
                    rs.getString("stock_quantity")
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error Loading Table: " + e.getMessage());
        }
    }
